const fs = require("fs");
const axios = require("axios");
const { execSync } = require("child_process");

const OPENAI_KEY = process.env.OPENAI_API_KEY;
const GEMINI_KEY = process.env.GEMINI_API_KEY;

function getIssue() {
  const event = JSON.parse(fs.readFileSync(process.env.GITHUB_EVENT_PATH, "utf8"));
  return event.issue.title + "\n" + (event.issue.body || "");
}

async function callOpenAI(task) {
  const res = await axios.post("https://api.openai.com/v1/chat/completions", {
    model: "gpt-5",
    messages: [
      { role: "system", content: "Return JSON with actions." },
      { role: "user", content: task }
    ]
  }, { headers: { Authorization: `Bearer ${OPENAI_KEY}` } });

  return res.data.choices[0].message.content;
}

async function selfFixLoop(task, retries = 2) {
  for (let i = 0; i < retries; i++) {
    let out = await callOpenAI(task);

    try {
      return JSON.parse(out);
    } catch {
      task += "\nFix JSON format";
    }
  }
  throw new Error("Failed after retries");
}

function apply(actions) {
  actions.forEach(a => {
    if (a.type === "create_file" || a.type === "update_file") {
      fs.mkdirSync(require("path").dirname(a.path), { recursive: true });
      fs.writeFileSync(a.path, a.content);
    }
  });
}

function gitPush(branch) {
  execSync(`git checkout -b ${branch}`);
  execSync("git add .");
  execSync('git commit -m "AI Level 4 auto changes" || echo skip');
  execSync(`git push origin ${branch}`);
}

async function createPR(branch) {
  const repo = process.env.GITHUB_REPOSITORY;
  const token = process.env.GITHUB_TOKEN;

  await axios.post(`https://api.github.com/repos/${repo}/pulls`, {
    title: "AI Automated PR",
    head: branch,
    base: "main",
    body: "Auto-generated by AI Level 4 system"
  }, {
    headers: { Authorization: `token ${token}` }
  });
}

async function main() {
  const task = getIssue();

  const parsed = await selfFixLoop(task);

  apply(parsed.actions);

  const branch = "ai-level4-" + Date.now();

  gitPush(branch);

  await createPR(branch);

  console.log("Level 4 complete");
}

main();
